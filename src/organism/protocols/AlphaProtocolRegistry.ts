export class AlphaProtocolRegistry {
  readonly definitions: readonly AlphaProtocolDefinition[] = ALPHA_PROTOCOL_DEFINITIONS;
  private readonly byId: ReadonlyMap<string, AlphaProtocolDefinition>;
  private readonly byTier: ReadonlyMap<AlphaProtocolTier, AlphaProtocolDefinition[]>;

  constructor() {
    const idMap = new Map<string, AlphaProtocolDefinition>();
    const tierMap = new Map<AlphaProtocolTier, AlphaProtocolDefinition[]>();
    for (const def of ALPHA_PROTOCOL_DEFINITIONS) {
      idMap.set(def.id, def);
      const list = tierMap.get(def.tier);
      if (list) list.push(def);
      else tierMap.set(def.tier, [def]);
    }
    this.byId = idMap;
    this.byTier = tierMap;
  }

  get(id: string): AlphaProtocolDefinition | undefined {
    return this.byId.get(id);
  }

  tier(tier: AlphaProtocolTier): AlphaProtocolDefinition[] {
    return this.byTier.get(tier) ?? [];
  }

  allCalls(): ProtocolCall[] {
    const calls: ProtocolCall[] = [];
    for (const def of ALPHA_PROTOCOL_DEFINITIONS) {
      calls.push(...def.calls);
    }
    return calls;
  }

  callCount(): number {
    return ALPHA_PROTOCOL_DEFINITIONS.reduce((sum, def) => sum + def.calls.length, 0);
  }

  stats(): {
    totalProtocols: number;
    totalCalls: number;
    byTier: Record<AlphaProtocolTier, number>;
  } {
    const byTier: Record<AlphaProtocolTier, number> = {
      core: 0,
      orchestration: 0,
      verification: 0,
      sovereign: 0,
      phantom: 0,
    };
    for (const def of ALPHA_PROTOCOL_DEFINITIONS) {
      byTier[def.tier]++;
    }
    return {
      totalProtocols: ALPHA_PROTOCOL_DEFINITIONS.length,
      totalCalls: this.callCount(),
      byTier,
    };
  }
}

export function createAlphaProtocolRegistry(): AlphaProtocolRegistry {
  return new AlphaProtocolRegistry();
}
